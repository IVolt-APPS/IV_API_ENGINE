using System.Reflection;

namespace IVolt.Examples.PluginEngine
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	An about. </summary>
	///
	/// <remarks>	Markalicz, 4/16/2025. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////
	public static class About
	{
		/////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the name of the application. </summary>
		///
		/// <value>	The name of the application. </value>
		/////////////////////////////////////////////////////////////////////////////////////////////
		public static string ApplicationName => "IV Plugin Engine Examples";

		/////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Gets the version of the application. </summary>
		///
		/// <value>	The version of the application. </value>
		/////////////////////////////////////////////////////////////////////////////////////////////
		public static string ApplicationVersion
		{
			get
			{
				var version = Assembly.GetExecutingAssembly().GetName().Version;
				return version?.ToString() ?? "Unknown";
			}
		}
	
	}
}
