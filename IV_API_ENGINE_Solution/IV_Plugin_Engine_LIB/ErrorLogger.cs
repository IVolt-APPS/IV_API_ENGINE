using IVolt.Apps.PluginEngine.Extensions;

namespace IVolt.Apps.PluginEngine
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	An error logger. </summary>
	///
	/// <remarks>	Markalicz, 4/16/2025. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////
	public static class ErrorLogger
	{
		/////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Logs. </summary>
		///
		/// <remarks>	Markalicz, 4/16/2025. </remarks>
		///
		/// <param name="msg">	The message. </param>
		/////////////////////////////////////////////////////////////////////////////////////////////
		private static void Log(string msg) => InternalHelpers.LogError(msg);
		/////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Logs an error. </summary>
		///
		/// <remarks>	Markalicz, 4/16/2025. </remarks>
		///
		/// <param name="context">	The context. </param>
		/// <param name="ex">	  	The exception. </param>
		/////////////////////////////////////////////////////////////////////////////////////////////
		public static void LogError(string context, Exception ex) => Log($"[PLUGIN ERROR] {context}: {ex.GetType().Name} - {ex.Message}");

	}
}
